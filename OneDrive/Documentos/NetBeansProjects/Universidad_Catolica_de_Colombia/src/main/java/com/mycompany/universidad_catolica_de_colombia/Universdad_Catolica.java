package com.mycompany.universidad_catolica_de_colombia;

/**
 *
 * @author diego
 */
public class Universdad_Catolica {
     private int Id_Estudiante;
     private String Nombres;
     private String Apellidos;
     
     public Universdad_Catolica (int Id_Estudiante,String Nombres,String Apellidos){
         
         this.Id_Estudiante=Id_Estudiante;
         this.Nombres=Nombres;
         this.Apellidos=Apellidos;
     }

    /**
     * @return the Id_Estudiante
     */
    public int getId_Estudiante() {
        return Id_Estudiante;
    }

    /**
     * @param Id_Estudiante the Id_Estudiante to set
     */
    public void setId_Estudiante(int Id_Estudiante) {
        this.Id_Estudiante = Id_Estudiante;
    }

    /**
     * @return the Nombres
     */
    public String getNombres() {
        return Nombres;
    }

    /**
     * @param Nombres the Nombres to set
     */
    public void setNombres(String Nombres) {
        this.Nombres = Nombres;
    }

    /**
     * @return the Apellidos
     */
    public String getApellidos() {
        return Apellidos;
    }

    /**
     * @param Apellidos the Apellidos to set
     */
    public void setApellidos(String Apellidos) {
        this.Apellidos = Apellidos;
    }
     
}
